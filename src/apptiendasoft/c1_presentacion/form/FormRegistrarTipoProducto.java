    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        // TODO add your handling code here:
        GestionarTipoProductoServicio gestionarTipoProductoServicio = new GestionarTipoProductoServicio();
        tipoProducto.setNombre(textoNombre.getText());
        tipoProducto.setDescripcion(textoAreaDescripcion.getText());
        try{
            if(tipoProducto.getCodigo()==0){
                if(verificarCamposVacios()){
                    gestionarTipoProductoServicio.crear(tipoProducto);
                    Mensaje.Mostrar_MENSAJE_GUARDADOEXITOSO(this);
                    this.dispose();
                }
            }else{
                gestionarTipoProductoServicio.modificar(tipoProducto);
                Mensaje.Mostrar_MENSAJE_MODIFICADOEXITOSO(this);
                this.dispose();
            }
        }catch(Exception e){
            Mensaje.mostrarErrorExcepcion(this, e.getMessage());
        }
    }//GEN-LAST:event_botonGuardarActionPerformed

