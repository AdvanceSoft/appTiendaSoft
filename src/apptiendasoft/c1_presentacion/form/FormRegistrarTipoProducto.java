    private void obtenerObjetoDeGestionar(TipoProducto tipoProducto){
        this.tipoProducto = tipoProducto;
        textoNombre.setText(tipoProducto.getNombre());
        textoAreaDescripcion.setText(tipoProducto.getDescripcion());
    }
    
    public boolean verificarCamposLlenos(){
        boolean verificar = false;
        if(!textoNombre.getText().trim().isEmpty())
           verificar = true;
        else if(!textoAreaDescripcion.getText().trim().isEmpty())
            verificar = true;
        return verificar;     
    }
    
    public boolean verificarCamposVacios(){
        boolean verificar = true;
        if(textoNombre.getText().trim().isEmpty()){
           Mensaje.Mostrar_MENSAJE_LLENARCAMPOSOBLIGATORIOS(this);
           textoNombre.requestFocus();
           verificar = false;
        }else if(textoAreaDescripcion.getText().trim().isEmpty()){
            Mensaje.Mostrar_MENSAJE_LLENARCAMPOSOBLIGATORIOS(this);
            textoAreaDescripcion.requestFocus();
            verificar = false;
        }
        return verificar;
    }
    
